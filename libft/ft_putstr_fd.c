#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	ret;

	ret = 0;
	if (!*s || fd == -1)
		return (0);
	i = -1;
	while (s[++i])
		ret = write(fd, &s[i], 1);
	return (ret);
}
